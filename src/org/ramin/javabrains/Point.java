package org.ramin.javabrains;

public class Point {
    private int x;

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    private int y;

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
